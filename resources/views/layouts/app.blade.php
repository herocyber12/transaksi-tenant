      function remove_cart(id) {
        var carts = JSON.parse(window.sessionStorage.getItem('carts')) ?? [];
        Swal.fire({
          title: 'Apakah Anda yakin ingin menghapus keranjang belanja Anda?',
          showCancelButton: true,
          confirmButtonText: 'Yakin'
        }).then((result) => {
          if (result.isConfirmed) {
            for (let i = 0; i < carts.length; i++) {
              if (i == id) {
                carts.splice(i, 1);
                Swal.fire('Berhasil!', 'Keranjang belanja berhasil dihapus.', 'success');
                window.location.reload();
              }
            }

            window.sessionStorage.setItem('carts', JSON.stringify(carts));
          }
        });
      }

